    <h1 style="font-size: 30px;">División de Salud Integral</h1><br /><br /><br />
    <font style="font-size: 15px; color: blue">Información general</font><br /><br />
La División de Salud Integral es una dependencia adscrita a la Vicerrectoría de Cultura y 
Bienestar que se encarga de brindar asistencia en salud a los estudiantes de la Universidad del Cauca.<br /><br />
    <font style="font-size: 15px; color: blue">Servicios</font><br /><br />
    Los servicios que presta la División de Salud Integral de la Universidad del Cauca son:
    <ul>
<li>Consulta odontológica general.</li>

<li>Consulta médica general.</li>

<li>Consulta psicológica general – psicoterapia y asesorías.</li>

<li>Procedimientos médicos y de enfermería.</li>

<li>Consulta en ginecología.</li>

<li>Orientación y valoración para el autoexamen de mama.</li>

<li>Talleres de promoción y prevención en salud mental.</li>

<li>Consulta en psiquiatría.</li>

<li>Consejería en salud sexual y reproductiva.</li>

<li>Orientación y atención de infecciones de transmisión sexual.</li>

<li>Toma de citologías.</li>

<li>Jornadas de higiene oral.</li>

<li>Orientación desde el área de Trabajo Social.</li>

<li>Adjudicación de residencias universitarias.</li>

<li>Servicio de cafeterías.</li>
</ul>
